/*

Operadores de Comparação

 > -> Maior
 < -> Menor

 >= -> Maior ou igual
 <= -> Menor ou igual   


*/

const firstNumber = 20
const secondNumber = 30

console.log(firstNumber > secondNumber)  // Maior
console.log(firstNumber < secondNumber)  // Menor 
console.log(firstNumber >= secondNumber) // Maior ou igual
console.log(firstNumber <= secondNumber) // Menor ou igual